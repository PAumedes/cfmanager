from cfmanager.services.dns import DNSService
from cfmanager.core.exceptions import ValidationError


# ── Export ────────────────────────────────────────────────────────────────────

def test_export_records_csv_returns_header_and_rows(mock_cloudflare_client):
    service = DNSService(mock_cloudflare_client)
    mock_records = [
        {"id": "r1", "name": "api.example.com", "type": "A", "content": "192.0.2.1", "ttl": 3600, "proxied": False, "comment": ""},
        {"id": "r2", "name": "www.example.com", "type": "CNAME", "content": "example.com", "ttl": 1, "proxied": True, "comment": "CDN"},
    ]
    service.list_dns_records = MagicMock(return_value=mock_records)

    result = service.export_records("zone_123", format="csv")

    lines = result.strip().splitlines()
    assert lines[0] == "name,type,content,ttl,proxied,comment"
    assert "api.example.com,A,192.0.2.1,3600,False," in result
    assert "www.example.com,CNAME,example.com,1,True,CDN" in result
    service.list_dns_records.assert_called_once_with("zone_123")


def test_export_records_bind_returns_zone_file_lines(mock_cloudflare_client):
    service = DNSService(mock_cloudflare_client)
    mock_records = [
        {"id": "r1", "name": "api.example.com", "type": "A", "content": "192.0.2.1", "ttl": 3600, "proxied": False, "comment": ""},
        {"id": "r2", "name": "mail.example.com", "type": "MX", "content": "10 mail.example.com.", "ttl": 300, "proxied": False, "comment": ""},
    ]
    service.list_dns_records = MagicMock(return_value=mock_records)

    result = service.export_records("zone_123", format="bind")

    assert "api.example.com 3600 IN A 192.0.2.1" in result
    assert "mail.example.com 300 IN MX 10 mail.example.com." in result


