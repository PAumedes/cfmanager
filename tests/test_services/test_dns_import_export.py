# ── Import ────────────────────────────────────────────────────────────────────

CSV_CONTENT = """name,type,content,ttl,proxied,comment
api.example.com,A,192.0.2.1,3600,false,
www.example.com,CNAME,example.com,3600,true,CDN"""


def test_import_records_csv_dry_run_parses_records(mock_cloudflare_client):
    service = DNSService(mock_cloudflare_client)

    result = service.import_records("zone_123", CSV_CONTENT, format="csv", dry_run=True)

    assert len(result) == 2
    assert result[0]["name"] == "api.example.com"
    assert result[0]["type"] == "A"
    assert result[0]["content"] == "192.0.2.1"
    assert result[0]["ttl"] == 3600
    assert result[0]["proxied"] is False
    assert result[1]["name"] == "www.example.com"
    assert result[1]["proxied"] is True
    assert result[1]["comment"] == "CDN"
    mock_cloudflare_client.sync_client.dns.records.create.assert_not_called()


def test_import_records_csv_creates_records(mock_cloudflare_client):
    service = DNSService(mock_cloudflare_client)

    mock_record = MagicMock()
    mock_record.id = "new_r1"
    mock_record.name = "api.example.com"
    mock_record.type = "A"
    mock_record.content = "192.0.2.1"
    mock_record.ttl = 3600
    mock_cloudflare_client.sync_client.dns.records.create.return_value = mock_record

    single_row = "name,type,content,ttl,proxied,comment\napi.example.com,A,192.0.2.1,3600,false,"
    result = service.import_records("zone_123", single_row, format="csv", dry_run=False)

    assert len(result) == 1
    assert result[0]["id"] == "new_r1"
    mock_cloudflare_client.sync_client.dns.records.create.assert_called_once_with(
        zone_id="zone_123",
        name="api.example.com",
        type="A",
        content="192.0.2.1",
        ttl=3600,
        proxied=False,
    )


