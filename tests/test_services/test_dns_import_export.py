def test_import_records_bind_four_token_ttl_line_skipped(mock_cloudflare_client):
    """A TTL-present BIND line with 4 tokens (no content) must be skipped, not produce empty content."""
    service = DNSService(mock_cloudflare_client)

    bind_content = """; valid line and one broken 4-token TTL-present line
api.example.com 3600 IN A 192.0.2.1
mail.example.com 300 IN A"""  # 4 tokens, else-branch, no content — must be skipped

    result = service.import_records("zone_123", bind_content, format="bind", dry_run=True)

    assert len(result) == 1
    assert result[0]["name"] == "api.example.com"
