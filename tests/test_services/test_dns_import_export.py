def test_import_records_bind_dry_run_parses_records(mock_cloudflare_client):
    service = DNSService(mock_cloudflare_client)

    bind_content = """; Generated by cfmanager
api.example.com 3600 IN A 192.0.2.1
www.example.com 1 IN CNAME example.com"""

    result = service.import_records("zone_123", bind_content, format="bind", dry_run=True)

    assert len(result) == 2
    assert result[0]["name"] == "api.example.com"
    assert result[0]["type"] == "A"
    assert result[0]["ttl"] == 3600
    assert result[1]["type"] == "CNAME"
    assert result[1]["content"] == "example.com"
    mock_cloudflare_client.sync_client.dns.records.create.assert_not_called()


def test_import_records_bind_skips_comments_and_directives(mock_cloudflare_client):
    service = DNSService(mock_cloudflare_client)

    bind_content = """; comment line
$ORIGIN example.com.
$TTL 3600
api.example.com 3600 IN A 192.0.2.1"""

    result = service.import_records("zone_123", bind_content, format="bind", dry_run=True)

    assert len(result) == 1
    assert result[0]["name"] == "api.example.com"


